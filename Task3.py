import networkx as nx

G = nx.Graph()
ukrainian_personalities = ["Тарас Шевченко", "Леся Українка", "Іван Франко", "Михайло Грушевський", "Олександр Довженко"]
G.add_nodes_from(ukrainian_personalities)
G.add_weighted_edges_from([
    ("Тарас Шевченко", "Леся Українка", 1),
    ("Тарас Шевченко", "Іван Франко", 3),
    ("Тарас Шевченко", "Михайло Грушевський", 2),
    ("Леся Українка", "Іван Франко", 2),
    ("Леся Українка", "Михайло Грушевський", 1),
    ("Іван Франко", "Михайло Грушевський", 4),
    ("Іван Франко", "Олександр Довженко", 5),
    ("Михайло Грушевський", "Олександр Довженко", 3)
])

# Алгоритм Дейкстри
shortest_paths = nx.single_source_dijkstra_path(G, "Тарас Шевченко")

# Результат
print("Найкоротші шляхи від Тараса Шевченка до інших вершин:")
for person, path in shortest_paths.items():
    print(f"{person}: {' -> '.join(path)}, вага={nx.single_source_dijkstra_path_length(G, 'Тарас Шевченко')[person]}")
